package game.utils;

/**
 * This enum is used to define the movement of the snake's head. Each enum represents a different direction and allows
 * for movement in any two-dimensinoal direction.
 */
public enum Direction {
    /** Value to represent upward movement */
    UP,

    /** Value to represent downward movement */
    DOWN,

    /** Value to represent leftward movement */
    LEFT,

    /** Value to represent rightward movement */
    RIGHT
}

